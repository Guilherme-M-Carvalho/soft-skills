package com.banco;

public class Cliente {
    private int agencia;
    private int numero;
    private String nome;
    
    public int getAgencia() {
        return agencia;
    }
    public int getNumero() {
        return numero;
    }
    public String getNome() {
        return nome;
    }
    public void setAgencia(int agencia) {
        this.agencia = agencia;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
